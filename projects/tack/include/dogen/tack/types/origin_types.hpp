#ifndef DOGEN_TACK_TYPES_ORIGIN_TYPES_HPP
#define DOGEN_TACK_TYPES_ORIGIN_TYPES_HPP

#if defined(_MSC_VER) && (_MSC_VER >= 1200)
#pragma once
#endif

namespace dogen {
namespace tack {

/**
 * @brief What originated the model element.
 *
 * System models are models such as STL, boost, etc. They provide a way to
 * expose third party code into Dogen so that we can make use of these types.
 * They need to be hand-coded in for the moment.
 *
 * User models are regular Dogen models, normally made using Dia.
 */
enum class origin_types : unsigned int {
    invalid = 0, ///< Represents an uninitialised enum
    user = 1, ///< The model element was created by the user
    system = 2, ///< The model element was generated by the system
    unknown = 3 ///< The origin of the element is not yet known
};

} }

#endif
